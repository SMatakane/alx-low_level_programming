#include "3-calc.h"

int op_add(int a, int b);
int op_sub(int a, int b);
int op_mul(int a, int b);
int op_div(int a, int b);
int op_mod(int a, int b);

/**
 * op_add - adds
 * @a: meh
 * @b: meh
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs
 * @a: meh
 * @b: meh
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 * @a: meh
 * @b: meh
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: meh
 * @b: meh
 * Return: dib
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: meh
 * @b: meh
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
